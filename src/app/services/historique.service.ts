import { HttpClient } from '@angular/common/http';
import { Injectable } from '@angular/core';
import { Observable } from 'rxjs';
import { apiUrlNode } from 'src/environments/environment';
import { Historique, PassageToInsert } from '../models/historique';

@Injectable({
  providedIn: 'root',
})
export class HistoriqueService {
  configUrl = apiUrlNode;

  constructor(private http: HttpClient) {}

  getHistoriques() {
    return this.http.get<Historique[]>(this.configUrl + 'historique');
  }

  getHistorique(id: String) {
    return this.http.get<Historique>(this.configUrl + 'historique/' + id);
  }

  checkIfMayBe(historique: Historique, dateTest: Date) {
    var dateHistorique = historique.date_passage;
    var dateHistoriquePossible = new Date(
      dateHistorique.getFullYear(),
      dateHistorique.getMonth(),
      dateHistorique.getDate() + 14
    );
    if (dateTest >= dateHistorique && dateTest <= dateHistoriquePossible) {
      return true;
    }
    return false;
  }

  addPassage(historique: PassageToInsert): Observable<any> {
    const headers = { 'content-type': 'application/json' };
    const body = JSON.stringify(historique);
    console.log("*******************");
    console.log(body);
    console.log(this.configUrl + 'historique');
    return this.http.post(this.configUrl + 'historique', body, {
      headers: headers,
    });
  }

  getHistoriqueByPersonne(id:String){
    return this.http.get<Historique[]>(this.configUrl + 'historiquePersonne/' + id);
  }

}

